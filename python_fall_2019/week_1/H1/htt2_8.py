# Write a program that will compute the area of a circle. Prompt the user to enter the radius and print a nice message back to the user with the answer.

# A = 3.14 * r * r

n = input('Enter the radius of a circle: ')

r = int(n)

pi = 3.14159

A = pi * (r * r)

print(A)
